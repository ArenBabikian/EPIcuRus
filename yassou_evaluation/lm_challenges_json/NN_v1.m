function data = NN_v1()

data = struct( ...
	'name', 'NN', ...
	'inputs', [ ...
		struct('name','x','type','Real'), ...
		struct('name','y','type','Real') ...
	], ...
	'outputs', [ ...
		struct('name','z','type','Real') ...
	], ...
	'requirements', [ ...
		struct( ...
			'rid', '1', ...
			'precondition', 'true', ...
			'phi', 'p1', ...
			'preds', [ ...
				struct('str','p1','A',[1],'b',1.1) ... % z <= 1.1
			] ...
		), ...
		struct( ...
			'rid', '2', ...
			'precondition', 'true', ...
			'phi', 'p2', ...
			'preds', [ ...
				struct('str','p2','A',[-1],'b',0.2) ... % -z <= 0.2 <=> z >= -0.2
			] ...
		) ...
	] ...
);

end
